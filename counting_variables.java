// Counting all the variables
public class counting_variables {
    // main() method start
    public static void main(String[] args) {
        // initialize counter
        int varcount = 0;

        // using for loop to increment the counter variable
        for (int i = 0; i < 3; i++) {
            // increment counter variable
            varcount = varcount +1;
            // print the incremented counter variable value
            System.out.println(varcount);
        }
    }
}

